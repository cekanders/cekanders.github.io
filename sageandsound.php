    <p>Another big decision that I had to make was how much content would be presented to the reader.
    After much deliberation, I decided that one large story would be presented to the viewer 
    upon entering the site and as they scrolled down, the text would transition into the next article
    in the final wireframe design. I was mainly inspired by Babe by Hatch website design. One reason why I made this decision was due to the nature and size of
    the platform. Because Sage and Sound is new, we didn't have too much content for the site at that time so
    it made sense to spread the content out instead of presenting everything on one page.</p>
    <div class='space'></div>
    <img class='img_home' src="images/landing2.png" alt="Landing 2" />

    <p>After a user scrolls through all of the featured articles, he/she is introduced 
    to the four categories of content and can browse through available articles in that category on the featured
    page or visit the category page. Through several iterations, I decided on presenting each category in a horiztonal row carousel. 
    By presenting a few articles for each category on the featured page, the user can is exposed to all of the categories before choosing which one he/she is most interested in.
    I also had to consider how much information should be presented on each article such as title, type 
    of content, author, date, etc. I decided to go with less information, only including the title and article 
    type to maintain the viewer's focus. Finally, I had to consider the placement and number of 
    email newsletter sign up boxes. Instead of having a pop-up box which could be potentially jarring
    and lack context, I decided on placing one after the user has been introduced to the content of the 
    blog as well as one in the footer of the page.</p>
