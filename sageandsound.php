        <p>WIREFRAMES</p>
    </div>

    <p>One challenge that I had to overcome while designing the wireframes for the landing page was 
    figuring out a way to categorize content. The copywriter created four different categories of 
    content which I considered separating into different pages which could be accessed by a dropdown subnavigation bar on the home page (Option 1) or 
    placing this secondary navigation bar on the blog page (Option 2). Because I wanted the user to explore all four 
    categories, I decided that a central landing page called Featured with a secondary nav bar would 
    increase engagement as this page would display popular articles from each category.
    However, a second horizontal nav bar directly below the first could be overwhelming for users. As a result, I 
    decided on placing a vertical nav bar on the left hand side next to a featured article to ensure its 
    prominence without being too overbearing.</p>

