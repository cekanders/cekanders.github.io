    <p>Our emails were sent to two types of customers: new customers and existing customers.
    As such, we had to tailor the content in the email to the needs of each group.
    New customers needed to be welcomed to both Blue Foundry Bank and AxisChecking while existing 
    customers only needed to be welcomed to AxisChecking.
