    <p>From here, I broke down the content by the business' primary objectives and secondary objectives. 
    The primary objectives are for the customer to set up direct deposit and electronic bill pay, as well as download the app. 
    For each objective, I mapped out all of the necessary steps and information for the user to complete the goal. 
    I took this hierarchy of information into account when designing the email templates.
