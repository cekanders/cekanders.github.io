    <div class="tag">
        <p>FINAL DESIGN</p>
    </div>
    <p>In designing the email series flow, my team and I had certain goals in mind. Our key objectives were 
    keeping the emails on-brand with differentiation, creating resuable components, and ensuring 
    designs are responsive and client-friendly. In this email series, there are three emails: Welcome Email, 
    Digital Banking Email, and Promotions Email. The goal of the first email is to get users to take 
    advantage of e-banking features. To do this, we give the customer actionable items to complete, 
    clearly organized in a numbered list. Each call-to-action is deeplinked to an external page while 
    secondary items are listed but not linked. The Digital Banking Email serves to enforce digital banking features in a different way through 
    a new layout and iconography. Finally, the promotions email focuses on quarterly promotions by 
    introducing familiar brands and actionable steps to activate these promotions.</p>
