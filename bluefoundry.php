    <p>I worked to design onboarding email templates for Blue Foundry Bank, a regional bank based in New Jersey.
        In this process, I conducted market research to understand the customers and their needs, created a customer journey map and iterated on template designs for two different products.
        My team and I developed templates for the AxisChecking account (personal) as well as Blue Carbon Banking (business). 
    </p>

    <div class="tag">
        <p>MARKET RESEARCH</p>
    </div>
    <p>Blue Foundry Bank is a regional bank targeting "movers and shakers," offering personal banking and business banking plans.
    Local banks, community banks with less than $10 billion in assets, often have smaller fees, support local economies, giving back to the community, yet 
    have a small lending capacity. On the other hand, larger banks such as regional and national banks have locations in many cities, attract people who want debit reward cards, 
    and move currency between countries faster. However, with rigid processes, these banks can be harder to contact.
    Consumers who are attracted to regional banks arenâ€™t interested in any specific account features (ex. mobile check deposit, mobile banking)
    and are more interested in looking for surcharge-free ATM.</p>


